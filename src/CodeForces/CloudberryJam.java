package CodeForces;

import java.util.Scanner;

public class CloudberryJam {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        while (num-- != 0) {
            int n=sc.nextInt();
            long ans=n*2;
            System.out.println(ans);
        }
    }
}
